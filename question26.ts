//The first version of if else statement will print the statement given in if block being "True"
let alienColor:string = "green"

if (alienColor == "green" ){
    console.log("the player just earned 5 points for shooting the alien")
}

else {
    console.log("the player just earned 10 points.")
   
}

//The second version of if else statement will print the statement given in the else block 
let alien_Color:string = "red"

if (alien_Color == "green" ){
    console.log("the player just earned 5 points for shooting the alien")
}

else {
    console.log("the player just earned 10 points.")
   
}


